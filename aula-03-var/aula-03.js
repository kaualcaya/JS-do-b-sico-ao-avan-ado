// let é uma forma de declarar variáveis em JavaScript (introduzida no ES6).
// Características do let:
// 1. Possui escopo de bloco (diferente de var, que tem escopo de função).
// 2. Pode ser atualizada, mas não redeclarada no mesmo escopo.
// 3. Evita problemas comuns com hoisting e reatribuição indevida.
// 4. Ideal para variáveis que mudarão de valor durante o código.

let nome = "Kauã Alcaya"
let idade = 21
let endereço = "Rua marcos arruda"

console.log("Meu nome é:",nome )